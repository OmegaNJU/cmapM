function mkgrp(fname, li)
% MKGRP make a GRP format file
%   MKGRP(FNAME, LIST) Creates a grp file FNAME using LIST which can be a
%   cell array of strings or a vector of numbers.
%
%   Format Details:
%   The GRP files contain a list in a simple newline-delimited
%   text format. Lines that start with a # are ignored.
%
%   See also: parse_grp

mkgrp(fname, li);

end
